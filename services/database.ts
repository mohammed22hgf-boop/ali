export const initializeDB = (): AppState => {
  const dbString = localStorage.getItem(DB_KEY);
  
  if (dbString) {
    try {
      const parsedDb = JSON.parse(dbString) as AppState;
      // If version is missing or outdated, reset the DB
      if (!parsedDb.version || parsedDb.version < DB_VERSION) {
        console.log(`Old DB version detected. Resetting database to v${DB_VERSION}.`);
        const initialState = createInitialState();
        localStorage.setItem(DB_KEY, JSON.stringify(initialState));
        return initialState;
      }
      // Make sure all settings objects have the new properties
      Object.keys(parsedDb.examSettings).forEach(subjectId => {
          if (parsedDb.examSettings[subjectId].questionCount === undefined) {
              parsedDb.examSettings[subjectId].questionCount = 60;
          }
          if (parsedDb.examSettings[subjectId].durationMinutes === undefined) {
              parsedDb.examSettings[subjectId].durationMinutes = 60;
          }
          if (parsedDb.examSettings[subjectId].allowRetakes === undefined) {
              parsedDb.examSettings[subjectId].allowRetakes = false;
          }
      });
      return parsedDb;
    } catch (e) {
      console.error("Failed to parse DB, resetting.", e);
      // If parsing fails, it might be corrupt, so reset.
      const initialState = createInitialState();
      localStorage.setItem(DB_KEY, JSON.stringify(initialState));
      return initialState;
    }
  } else {
    // If no DB exists, create a new one.
    const initialState = createInitialState();
    localStorage.setItem(DB_KEY, JSON.stringify(initialState));
    return initialState;
  }
};

export const getDB = (): AppState => {
  const db = localStorage.getItem(DB_KEY);
  if (!db) return initializeDB();
  return JSON.parse(db);
};

export const saveDB = (state: AppState) => {
  localStorage.setItem(DB_KEY, JSON.stringify(state));
};